package com.example.bctn.domain;

public class thongke {
    String Ten;
    double Tien;

    public thongke(String ten, double tien) {
        Ten = ten;
        Tien = tien;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }

    public double getTien() {
        return Tien;
    }

    public void setTien(double tien) {
        Tien = tien;
    }
}
